public class ViewTagObj {
    public float x_in;
    public float x_out;
    public float y_in;
    public float y_out;
    public float a_in;
    public float a_out;

    @Override
    public String toString() {
        return "x_in : " + x_in + " x_out : " + x_out
                + " y_in : " + y_in + " y_out : " + y_out
                + " a_in : " + a_in + " a_out: " + a_out;
    }
}
